CREATE DATABASE lab_mysql;
USE lab_mysql;
CREATE TABLE cars ( Vin INT, manufacturer VARCHAR(45), MODEL VARCHAR(45), YEAR DATE);
CREATE TABLE customers ( idCustomers INT, name VARCHAR(45), phone_number VARCHAR(45), email VARCHAR(45);
CREATE TABLE salesperson ( staff ID INT, name VARCHAR(45), store_of_the_company VARCHAR(45);
CREATE TABLE invoices ( invoice_number INT, date DATE, car VARCHAR(45), customer VARCHAR(45), sales_person_of_car VARCHAR(45);